// ---------- Today's predicted accident hotspots ----------
//
// Auto-uses the live Atlanta weather and the recent-activity feedback loop.
router.get("/atlanta/today-accident-predictions", async (req, res): Promise<void> => {
  res.setHeader("Cache-Control", "no-cache, no-store, must-revalidate");
  res.setHeader("Pragma", "no-cache");
  res.setHeader("Expires", "0");
  try {
    // Normalize the includePastHours query before Zod parses it: orval emits
    // `zod.coerce.boolean()` which treats any non-empty string as true (so
    // "false" would become true). Mapping to a real boolean here keeps the
    // parsed schema honest for the rest of the handler.
    const rawInclude = req.query.includePastHours;
    const includePastBool = rawInclude === "true" || rawInclude === "1";
    const parsed = GetTodayAccidentPredictionsQueryParams.safeParse({
      topN: req.query.topN,
      includePastHours: includePastBool,
    });
    if (!parsed.success) {
      res.status(400).json({
        error: "Invalid query parameters",
        detail: parsed.error.issues.map((i) => `${i.path.join(".")}: ${i.message}`).join("; "),
      });
      return;
    }
    const { topN, includePastHours } = parsed.data;

    // Live weather drives the model — degrade gracefully to "clear" if the
    // upstream is unreachable so we still return a useful prediction. When
    // available, we ALSO derive a 24-element continuous multiplier from the
    // hourly precip/wind/temp outlook so per-hour predictions reflect when
    // bad weather actually arrives, not just a single all-day bucket.
    let weather: "clear" | "light_rain" | "heavy_rain" | "snow" = "clear";
    let weatherSummary = "clear (weather feed unavailable)";
    let hourlyWeatherMults: number[] | undefined;
    try {
      const wx = await getLiveWeather();
      weather = wx.condition;
      weatherSummary = wx.summary;
      hourlyWeatherMults = computeHourlyWeatherMultipliers(wx);
    } catch (e) {
      req.log.warn({ err: e }, "today-accident-predictions: weather fetch failed, defaulting to clear");
    }

    const todayDate = todayInAtlanta();
    const dow = new Date(`${todayDate}T12:00:00Z`).getUTCDay();
    const isWeekday = dow >= 1 && dow <= 5;
    const currentHourLocal = currentHourInAtlanta();

    const result = predictTodayAccidents({
      date: todayDate,
      weather,
      weatherSummary,
      events: { schoolDay: isWeekday },
      topN,
      includePastHours,
      currentHourLocal,
      hourlyWeatherMults,
    });
    res.json(GetTodayAccidentPredictionsResponse.parse(result));
  } catch (e) {
    req.log.error({ err: e }, "today-accident-predictions failed");
    res.status(500).json({
      error: "Failed to compute today's accident predictions",
      detail: e instanceof Error ? e.message : String(e),
    });
  }
});

// Current hour-of-day in America/New_York (0..23).
function currentHourInAtlanta(): number {
  const fmt = new Intl.DateTimeFormat("en-US", {
    timeZone: "America/New_York",
    hour: "numeric",
    hour12: false,
  });
  const parts = fmt.formatToParts(new Date());
  const h = parts.find((p) => p.type === "hour")?.value;
  const n = h ? Number(h) : new Date().getHours();
  // "24" can show up at midnight in some Node versions; normalize.
  return ((n % 24) + 24) % 24;
}

// Today's date in America/New_York as YYYY-MM-DD. Uses Intl to handle DST.
function todayInAtlanta(): string {
  const fmt = new Intl.DateTimeFormat("en-CA", {
    timeZone: "America/New_York",
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  });
  return fmt.format(new Date());
}

