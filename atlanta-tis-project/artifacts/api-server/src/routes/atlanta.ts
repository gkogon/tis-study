// ---------- Apple-Maps-style live traffic flow (synthesized, no API key) ----------

router.get("/atlanta/live-traffic-flow", async (req, res): Promise<void> => {
  try {
    const raw = typeof req.query.classes === "string" ? req.query.classes : "";
    const classes = raw
      .split(",")
      .map((s) => Number(s.trim()))
      .filter((n) => Number.isInteger(n) && n >= 0 && n <= 3);
    const bundle = await getLiveTrafficFlow(classes.length ? { classes } : undefined);
    // Allow conditional GETs to short-circuit with a 304: the bundle's
    // fetchedAt + classes set + segment count uniquely identifies the
    // payload, and it only changes when our 60s server cache rolls. This
    // saves the ~3MB body on the common case where the client polls inside
    // the same minute as a previous request (refresh, second tab, etc).
    const classKey = classes.length ? classes.slice().sort().join(",") : "default";
    const etag = `W/"ltf-${classKey}-${bundle.fetchedAt}-${bundle.segments.length}"`;
    res.setHeader("ETag", etag);
    res.setHeader("Last-Modified", new Date(bundle.fetchedAt).toUTCString());
    // 60s freshness matches our internal cache TTL; require revalidation
    // afterwards so clients always pick up the new compute when it lands.
    res.setHeader("Cache-Control", "private, max-age=60, must-revalidate");
    if (req.headers["if-none-match"] === etag) {
      res.status(304).end();
      return;
    }
    res.json(GetLiveTrafficFlowResponse.parse(bundle));
  } catch (e) {
    req.log.error({ err: e }, "live-traffic-flow failed");
    res.status(500).json({
      error: "Failed to compute live traffic flow",
      detail: e instanceof Error ? e.message : String(e),
    });
  }
});

