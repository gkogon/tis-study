// Returns today's accuracy snapshot (predicted top-N vs signals that actually
// saw a live incident today), updates the persisted history file, and includes
// the rolling 7-day stats. The snapshot also drives the per-signal recent-
// activity multiplier baked into future predictDay() calls.
router.get("/atlanta/accuracy", async (req, res): Promise<void> => {
  res.setHeader("Cache-Control", "no-cache, no-store, must-revalidate");
  res.setHeader("Pragma", "no-cache");
  res.setHeader("Expires", "0");
  try {
    const topNRaw = Number(req.query.topN);
    const topN = Number.isFinite(topNRaw)
      ? Math.max(10, Math.min(1000, Math.trunc(topNRaw)))
      : 50;

    // 1) Pull today's live incidents (cached server-side for 90s).
    const live = await getLiveIncidents();

    // 1a) Degraded-mode guard: if the upstream live feed has clearly failed
    //     (zero items in metro AND zero total fetched), do NOT persist a
    //     misleading "0 incidents today" snapshot — that would corrupt the
    //     7-day rolling stats and starve the recent-activity feedback loop.
    //     Surface the degradation honestly to the caller.
    const upstreamDegraded = live.totalFetched === 0;
    if (upstreamDegraded) {
      // Return last-good summary if we have history, otherwise a clear empty
      // report. Either way, do not call recordSnapshot for today.
      const summary = getAccuracySummary(todayInAtlanta());
      const todayDate = todayInAtlanta();
      const placeholder: DaySnapshot = {
        date: todayDate,
        observedAt: new Date().toISOString(),
        predictedTopRiskOsmIds: [],
        topNUsed: 0,
        actualIncidentSignalCounts: {},
        actualIncidentTotal: 0,
        hits: 0,
        hitRatePct: 0,
        precisionPct: 0,
      };
      const report = {
        today: placeholder,
        recentDays: summary.recentDays,
        rollingHitRatePct: summary.rollingHitRatePct,
        rollingPrecisionPct: summary.rollingPrecisionPct,
        liveIncidentsTotal: 0,
        liveIncidentsSnapped: 0,
        recentActivitySignalCount: getRecentActivityMultipliers().size,
        upstreamDegraded: true,
      };
      req.log.warn(
        { fetchedAt: live.fetchedAt },
        "accuracy: upstream live feed returned 0 items — skipping snapshot persistence",
      );
      res.status(200).json(GetPredictionAccuracyResponse.parse(report));
      return;
    }

    // 2) Compute today's prediction with a sensible default scenario:
    //    live weather (continuous hourly multipliers), school day if
    //    weekday, no special events. Using continuous weather here gives
    //    the prediction the same precip/wind/temp signal that the predicted
    //    traffic flow uses, so the accuracy hit rate measures the actual
    //    deployed model — not a "clear weather" approximation of it.
    const todayDate = todayInAtlanta();
    const dow = new Date(`${todayDate}T12:00:00Z`).getUTCDay();
    const isWeekday = dow >= 1 && dow <= 5;
    let liveHourlyWeatherMults: number[] | undefined;
    let liveCondition: "clear" | "light_rain" | "heavy_rain" | "snow" = "clear";
    try {
      const wx = await getLiveWeather();
      liveCondition = wx.condition;
      liveHourlyWeatherMults = computeHourlyWeatherMultipliers(wx);
    } catch (e) {
      req.log.warn({ err: e }, "accuracy: weather fetch failed, falling back to clear");
    }
    const prediction = predictDay({
      date: todayDate,
      weather: liveCondition,
      events: { schoolDay: isWeekday },
      hourlyWeatherMults: liveHourlyWeatherMults,
    });

    // 3) Build predicted top-N OSM ids (the model's "high-risk shortlist").
    //    Sort the full signal list ONCE, dedupe FIRST, then slice to exactly
    //    topN — otherwise duplicates between prediction.topWorst and the full
    //    sorted list consume slots and shrink topNUsed below the requested
    //    value (deflating the precision denominator and confusing the UI).
    const sortedAll = [...prediction.signals].sort(
      (a, b) => b.predictedPeakScore - a.predictedPeakScore,
    );
    const seenIds = new Set<number>();
    const uniqueTopRisk: number[] = [];
    for (const s of sortedAll) {
      const osmId = Number(s.id.replace(/^ATL-/, ""));
      if (!Number.isFinite(osmId) || seenIds.has(osmId)) continue;
      seenIds.add(osmId);
      uniqueTopRisk.push(osmId);
      if (uniqueTopRisk.length >= topN) break;
    }

    // 4) Aggregate today's actual incidents per snapped signal.
    const actualCounts: Record<string, number> = {};
    for (const inc of live.incidents) {
      if (!inc.signalId) continue;
      const osmIdStr = String(Number(inc.signalId.replace(/^ATL-/, "")));
      actualCounts[osmIdStr] = (actualCounts[osmIdStr] ?? 0) + 1;
    }
    const actualSignalIds = new Set(Object.keys(actualCounts).map(Number));
    const topRiskSet = new Set(uniqueTopRisk);
    let hits = 0;
    for (const id of actualSignalIds) if (topRiskSet.has(id)) hits++;
    const denomHit = actualSignalIds.size || 0;
    const hitRatePct = denomHit > 0 ? Math.round((hits / denomHit) * 1000) / 10 : 0;
    const precisionPct = uniqueTopRisk.length > 0
      ? Math.round((hits / uniqueTopRisk.length) * 1000) / 10
      : 0;

    // 5) Persist (upsert) today's snapshot.
    const snapshot: DaySnapshot = {
      date: todayDate,
      observedAt: new Date().toISOString(),
      predictedTopRiskOsmIds: uniqueTopRisk,
      topNUsed: uniqueTopRisk.length,
      actualIncidentSignalCounts: actualCounts,
      actualIncidentTotal: live.incidents.length,
      hits,
      hitRatePct,
      precisionPct,
    };
    recordSnapshot(snapshot);

    // 6) Assemble the accuracy report (today + recent days + rolling stats).
    const summary = getAccuracySummary(todayDate);
    const report = {
      today: snapshot,
      recentDays: summary.recentDays,
      rollingHitRatePct: summary.rollingHitRatePct,
      rollingPrecisionPct: summary.rollingPrecisionPct,
      liveIncidentsTotal: live.incidents.length,
      liveIncidentsSnapped: live.snappedToSignal,
      recentActivitySignalCount: getRecentActivityMultipliers().size,
      upstreamDegraded: false,
    };
    res.json(GetPredictionAccuracyResponse.parse(report));
  } catch (e) {
    req.log.error({ err: e }, "accuracy compute failed");
    res.status(502).json({
      error: "Failed to compute prediction accuracy",
      detail: e instanceof Error ? e.message : String(e),
    });
  }
});

