router.get("/atlanta/parking/lots", (req, res): void => {
  const forceSurge = req.query.forceSurge === "true" || req.query.forceSurge === "1";

  let minCapacity = req.query.minCapacity !== undefined
    ? parseInt(String(req.query.minCapacity), 10)
    : NaN;
  if (Number.isFinite(minCapacity) && minCapacity < 0) {
    // Mirror the OpenAPI `minimum: 0` constraint at runtime.
    res.status(400).json({ error: "minCapacity must be >= 0" });
    return;
  }

  const maxFillPct = req.query.maxFillPct !== undefined
    ? parseFloat(String(req.query.maxFillPct))
    : NaN;
  if (Number.isFinite(maxFillPct) && (maxFillPct < 0 || maxFillPct > 100)) {
    res.status(400).json({ error: "maxFillPct must be between 0 and 100" });
    return;
  }

  const archetype = typeof req.query.archetype === "string" ? req.query.archetype : null;
  if (archetype !== null && !(ARCHETYPE_LABELS as readonly string[]).includes(archetype)) {
    res.status(400).json({ error: "Invalid archetype" });
    return;
  }

  let lots = getAllLotsView(forceSurge);
  if (Number.isFinite(minCapacity)) lots = lots.filter((l) => l.capacity >= minCapacity);
  if (Number.isFinite(maxFillPct)) lots = lots.filter((l) => l.fillPct <= maxFillPct);
  if (archetype) lots = lots.filter((l) => l.archetype === archetype);
  res.setHeader("Cache-Control", "public, max-age=60");
  res.json(lots);
});

