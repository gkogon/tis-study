// ---------- Predicted traffic flow (tomorrow's road map) ----------

router.get("/atlanta/predicted-traffic-flow", async (req, res): Promise<void> => {
  const date = String(req.query.date ?? "");
  const hourRaw = Number(req.query.hour);
  if (!date || !/^\d{4}-\d{2}-\d{2}$/.test(date)) {
    res.status(400).json({ error: "date query param required (YYYY-MM-DD)" });
    return;
  }
  if (!Number.isFinite(hourRaw) || hourRaw < 0 || hourRaw > 23) {
    res.status(400).json({ error: "hour query param required (0-23)" });
    return;
  }
  const weatherRaw = String(req.query.weather ?? "clear");
  const weather = (["clear", "light_rain", "heavy_rain", "snow"].includes(weatherRaw)
    ? weatherRaw
    : "clear") as "clear" | "light_rain" | "heavy_rain" | "snow";
  const events = {
    schoolDay: req.query.schoolDay === "true",
    falconsHome: req.query.falconsHome === "true",
    hawksHome: req.query.hawksHome === "true",
    bravesHome: req.query.bravesHome === "true",
    gtFootball: req.query.gtFootball === "true",
    holiday: req.query.holiday === "true",
  };
  const classesRaw = String(req.query.classes ?? "0,1,2");
  const classes = classesRaw.split(",").map(Number).filter((n) => Number.isInteger(n) && n >= 0 && n <= 3);
  if (classes.length === 0) classes.push(0, 1, 2);

  // When the user explicitly picked a weather scenario from the UI, respect
  // that choice (use the discrete bucket multiplier). Otherwise — and ONLY
  // when the requested date is today's date in Atlanta — replace the bucket
  // with the continuous per-hour multipliers from Open-Meteo. The live feed
  // requests `forecast_days=1` so its hourly array only covers today;
  // applying it to tomorrow/future dates would attribute today's weather
  // pattern to the wrong day.
  let hourlyWeatherMults: number[] | undefined;
  const userPickedWeather = typeof req.query.weather === "string"
    && req.query.weather !== "clear";
  if (!userPickedWeather && date === todayInAtlanta()) {
    try {
      const wx = await getLiveWeather();
      hourlyWeatherMults = computeHourlyWeatherMultipliers(wx);
    } catch (e) {
      req.log.warn({ err: e }, "predicted-traffic-flow: weather fetch failed, using bucket fallback");
    }
  }

  try {
    const bundle = getPredictedTrafficFlow({
      date,
      weather,
      events,
      hour: Math.trunc(hourRaw),
      classes,
      hourlyWeatherMults,
    });
    res.json(GetPredictedTrafficFlowResponse.parse(bundle));
  } catch (e) {
    req.log.error({ err: e }, "predicted-traffic-flow failed");
    res.status(500).json({ error: e instanceof Error ? e.message : String(e) });
  }
});

